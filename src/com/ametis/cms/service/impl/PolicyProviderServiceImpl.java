	public Collection<PolicyProvider> searchMemberProvider(String q,
			Integer policyId, Integer memberId) throws Exception {
		Collection<PolicyProvider> result = new Vector<PolicyProvider>();
				
				try {
		
					Session session = policyProviderDao.getPolicyProviderSession();
					String sqlQuery = "SELECT p FROM "
						+ "PolicyProvider p WHERE p.providerId.providerName LIKE :keyword AND " +
								"p.deletedStatus = 0 AND p.AND p.policyId.policyId = :policyId " +
								" AND p.providerId.providerId NOT IN (SELECT mp.providerId FROM " +
								"MemberProvider mp WHERE mp.memberId.memberId = :memberId) ORDER BY p.providerId.providerName ASC";
			
					Query query = session.createQuery(sqlQuery);
					query.setString("keyword", "%" + q + "%");
					query.setInteger("policyId", policyId);
					query.setInteger("memberId", memberId);
					query.setMaxResults(10);
					
					
					result = query.list();
					
				}
				catch (Exception e){
					e.printStackTrace();
				}
				
				return result;
	}
	@Override
	public PolicyProvider createUpload(PolicyProvider object,
			MultipartFile file, ActionUser user) throws Exception {
		
		Configuration config = configurationService.getSystemConfiguration();
		
		String path = "";
		
		if (config != null){
			path = config.getUploadStorageDirectory();
		}
		String fileName = "";
		
		if (file != null){
			fileName = path + System.getProperty("file.separator") + System.currentTimeMillis()+"_" + file.getOriginalFilename();
		}
		
		object.setUrl(fileName);
		object.setDeletedStatus(1);
		object.setType("upload");
		object.setStatusUpdate(0);
		
		
		object.setCreatedTime (new java.sql.Timestamp (System.currentTimeMillis()));
							
		if (user != null && user.getUser() != null){
			object.setCreatedBy(user.getUser().getUsername());
		}		

		PolicyProvider result = policyProviderDao.create (object);
		
		if (result != null && file != null){
			try {
				FileOutputStream outstream = new FileOutputStream(object.getUrl());;
				outstream.write(file.getBytes());
				outstream.flush();
				outstream.close();
			}
			catch (Exception e){
				e.printStackTrace();
			}
		}
		return result;

	}




