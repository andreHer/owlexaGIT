	public boolean approve(Serializable id, ActionUser user) throws Exception {
		// TODO Auto-generated method stub
		boolean result = false;
		try {
	
			CardStock stock = get(id);
			
			if (stock != null){
				Client client = clientService.get(stock.getClientId().getClientId());
				stock.setStatus(1);
				
				if (client != null){
					int currentStock = client.getCurrentCardStock() == null ? 0 : client.getCurrentCardStock().intValue();
					int additionalStock = stock.getAdditionalStock() == null ? 0 : stock.getAdditionalStock().intValue();
					stock.setInitialStock(currentStock);
					int remaining = currentStock + additionalStock;
					stock.setRemainingStock(remaining);
					
					client.setCurrentCardStock(remaining);
					
					update(stock,user);
					
					clientService.update(client, user);
				}
				
			}
			ActivityLog log = new ActivityLog();
			log.setAction("APPROVE CARD STOCK");
			log.setDescription("CARD STOCK ID = " + id);
			log.setActivityLogTime(new java.sql.Timestamp(System.currentTimeMillis()));
			log.setIpAddress(user.getIpAddress());
			log.setUsername(user.getUser().getUsername());
			log.setSessionId(user.getLoginSession());
			
			logService.create(log);
		}
		catch (Exception e){
			e.printStackTrace();
		}
		return result;
		
	}




