	public boolean addMedicine(Integer policyId,Collection<PolicyMedicine> medicineList, ActionUser user)
			throws Exception {
		// TODO Auto-generated method stub
		boolean result = false;
		
		try {
			if (policyId != null && medicineList != null){
				Policy policy = policyService.get(policyId);
				
				for (Iterator iterator = medicineList.iterator(); iterator
						.hasNext();) {
					
					PolicyMedicine med = (PolicyMedicine) iterator.next();
					
					med.setPolicyId(policy);					
					create(med,user);
					
					
				}
				result = true;
			}

		}
		catch (Exception e){
			e.printStackTrace();
		}
		
		return result;
	}

