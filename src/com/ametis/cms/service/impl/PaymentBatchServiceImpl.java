	@Override
	public boolean confirmPayment(Integer batchId, String approvalNote, Date confirmDate,
			ActionUser user) throws Exception {
		// TODO Auto-generated method stub
		
		boolean result = false;
		
		PaymentBatch batch = get(batchId);
		
		if (batch != null){
			PaymentStatus status = new PaymentStatus();
			status.setPaymentStatusId(PaymentStatus.PAYMENT_PAID);
			
			batch.setPaymentStatus(status);
			batch.setBatchStatus(1);
			batch.setApprovalNote(approvalNote);
			batch.setApprovedTime(new java.sql.Timestamp(System.currentTimeMillis()));
			batch.setPaymentBatchConfirmDate(confirmDate);
			
			if (user != null && user.getUser() != null){
				batch.setApprovedBy(user.getUser().getUsername());
			}
			
			update(batch,user);
			
			Collection<PaymentInstallment> installmentList = paymentInstallmentService.getInstallmentList(batchId);
			
			for (Iterator iterator = installmentList.iterator(); iterator
					.hasNext();) {
				
				PaymentInstallment paymentInstallment = (PaymentInstallment) iterator.next();
				
				if (paymentInstallment != null){
					paymentInstallmentService.confirmPayment(paymentInstallment.getPaymentInstallmentId(), approvalNote,confirmDate, user);
				}
				
				Payment payment = paymentService.get(paymentInstallment.getPaymentId().getPaymentId());
				double totalPaid = 0.0;
				
				if (payment.getPaymentStatus().getPaymentStatusId().intValue() == PaymentStatus.PAYMENT_DISPOSITION){
					int totalDisposition = paymentInstallmentService.getTotalInstallmentDisposition(paymentInstallment.getPaymentId().getPaymentId());
					
					if (totalDisposition == 0){
						payment.setPaymentStatus(status);
						payment.setPaymentApprovalTime(new java.sql.Timestamp(System.currentTimeMillis()));
						
						paymentService.finalizeFromBatch(payment, user);
						
					}
				}
				
			}
			
			result = true;
		}
		
		return result;
	}




