	public PaymentBatch create (PaymentBatch object,Collection<Integer> paymentList,ActionUser user) throws Exception {
			
			
//			object.setCreatedDate (new java.sql.Date (System.currentTimeMillis()));

			// ----

			Configuration configuration = configurationService.getSystemConfiguration();
			
			String paymentBatchNumber = "";
			
			if (configuration != null){
				
				if (configuration.getOtherPaymentNumber() != null){
					int currentOther = configuration.getOtherPaymentNumber();
					
				}
			}
		
			
			
			
			object.setDeletedStatus(new Integer(0));
			object.setBatchStatus(Integer.valueOf(0));

			
			
						
			if (user != null){
				object.setCreatedBy(user.getUser().getUsername());
			}
			

			PaymentBatch result = paymentBatchDao.create (object);
			
			double totalBatch = 0.0;
			if (result != null){
				if (paymentList != null){
					Iterator<Integer> iterator = paymentList.iterator();
					
					while(iterator.hasNext()){
						Integer paymentId = iterator.next();
						PaymentInstallment payment = paymentInstallmentService.get(Long.valueOf(paymentId) );
						
						if (payment != null){
							totalBatch += payment.getTotalValue();
							
							payment.setPaymentBatchingStatus(Integer.valueOf(1));
							payment.setPaymentBatchId(result);
							
							paymentInstallmentService.update(payment, user);
						}
					}
					/**
					while(iterator.hasNext()){
						Integer paymentId = iterator.next();
						Payment payment = paymentService.get(paymentId);
						
						if (payment != null){
							totalBatch += payment.getPaymentValue();
							
							payment.setPaymentBatchingStatus(Integer.valueOf(1));
							payment.setPaymentBatchId(result);
							
							paymentService.update(payment, user);
						}
					}*/
				}
			}
			
			result.setOutstandingPayment(totalBatch);
			result.setPaidPayment(0.0);
			result.setTotalPayment(totalBatch);
			return result;
		}
