	public boolean addMedicine(Integer caseId,
			Collection<CaseMedicine> medicineList, ActionUser user)
			throws Exception {
		boolean result = false;
		
		try {
			Case theCase = caseService.get(caseId);
			
			if (theCase != null){
			
				for (Iterator iterator = medicineList.iterator(); iterator.hasNext();) {
					
					CaseMedicine caseMedicine = (CaseMedicine) iterator.next();
					caseMedicine.setCaseId(theCase);
					
					create(caseMedicine,user);
					
				}
				result = true;
			}
		}
		catch (Exception e){
			e.printStackTrace();
		}
		return result;
	}


