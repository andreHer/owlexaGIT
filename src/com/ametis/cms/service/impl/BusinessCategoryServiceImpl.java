	/*
	* Method delete (BusinessCategory object) berfungsi untuk melakukan penghapusan terhadap
	* sebuah object yang terdapat didalam database
	* @param object adalah sebuah object yang ingin dihapus, isi dari object tersebut cukup dengan
	*		 mengisi field-field primary key
	* @return updated object, exception if failed
	*/

	public BusinessCategory delete (java.io.Serializable pkey,ActionUser user) throws Exception{
		BusinessCategory object = businessCategoryDao.get (pkey);


		
		
		
		object.setDeletedTime (new java.sql.Timestamp (System.currentTimeMillis()));

		
		
		
		object.setDeletedStatus(new Integer(1));

		


		
			
		if (user != null && user.getUser() != null){
			object.setDeletedBy(user.getUser().getUsername());
		}

		
		businessCategoryDao.update (object);
		return object;
	}


