	public ProductClausul create (ProductClausul object,ActionUser actionUser) throws Exception {
			
			
//			object.setCreatedDate (new java.sql.Date (System.currentTimeMillis()));

			// ----

			
			object.setCreatedTime(new java.sql.Timestamp(System.currentTimeMillis()));
			object.setDeletedStatus(Integer.valueOf(0));

			
			
			if (actionUser != null){
				User user = actionUser.getUser();					
			if (user != null){
				object.setCreatedBy(user.getUsername());
			}
			}

			if (object.getDiagnosisId() != null){
				Diagnosis diagnosis = diagnosisService.get(object.getDiagnosisId().getDiagnosisId());
				if (diagnosis != null){
					object.setDiagnosisCode(diagnosis.getDiagnosisCode());
					object.setDiagnosisName(diagnosis.getDescription());
				}
				object.setIsDiagnosis(1);
			}
			else {
				object.setIsDiagnosis(0);
			}

			ProductClausul result = productClausulDao.create (object);
			return result;
		}
