	public Collection<Procedure> searchProcedure(String q) throws Exception {
		// TODO Auto-generated method stub
		Collection<Procedure> result = new Vector<Procedure>();
		
		try {
			Session session = procedureDao.getProcedureSession();
			String sqlQuery = "SELECT p FROM "
				+ "Procedure p WHERE p.procedureName LIKE :keyword AND p.deletedStatus = 0 ORDER BY p.procedureName ASC";
	
			Query query = session.createQuery(sqlQuery);
			query.setString("keyword", "%" + q + "%");
			query.setMaxResults(15);
			
			
			result = query.list();
		}
		catch (Exception e){
			e.printStackTrace();
		}
		return result;
	}




