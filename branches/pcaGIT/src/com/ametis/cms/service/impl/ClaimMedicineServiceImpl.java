	public boolean addMedicine(Integer claimId,
			Collection<ClaimMedicine> medicineList, ActionUser user)
			throws Exception {
		boolean result = false;
		
		try {
			Claim claim = claimService.get(claimId);
			double totalDrugs = 0.0;
			
			if (claim != null){			
				for (Iterator iterator = medicineList.iterator(); iterator.hasNext();) {
					
					ClaimMedicine claimMedicine = (ClaimMedicine) iterator.next();
					claimMedicine.setClaimId(claim);
					
					create(claimMedicine,user);
					
					
					
				}
				String[] eqParamMed = {"claimId.claimId","deletedStatus"};
				Object[] eqValueMed = {claim.getClaimId(),0};
				int totalMed = getTotal(null,null,eqParamMed,eqValueMed);
				Collection<ClaimMedicine> claimMedList = search(null,null,eqParamMed,eqValueMed,0,totalMed);
				
				for (Iterator iterator = claimMedList.iterator(); iterator
						.hasNext();) {
					
					ClaimMedicine claimMedicine = (ClaimMedicine) iterator
							.next();
					
					if (claimMedicine != null){
						totalDrugs += claimMedicine.getTotalCharge();
					}
					
				}
				result = true;
				
				if (totalDrugs > 0.0){
					
					String[] eqParam = {"claimId.claimId","deletedStatus","itemId.itemId"};
					Object[] eqValue = {claim.getClaimId(),Integer.valueOf(0),Item.DRUGS_OP};
					
					Collection<ClaimItem> claimItemList = claimItemService.search(null,null,eqParam,eqValue,0,1);
					
					if (claimItemList != null && claimItemList.size() > 0){
						Iterator<ClaimItem> iterator = claimItemList.iterator();
						if (iterator.hasNext()){
							ClaimItem claimItem = iterator.next();
							
							if (claimItem != null){

								claimItem.setClaimItemValue(totalDrugs);
								claimItemService.update(claimItem, user);
							}
						}
					}
					else {
						Item item = new Item();
						item.setItemId(Item.DRUGS_OP);
						
						ClaimItem claimItem = new ClaimItem();
						claimItem.setClaimId(claim);
						claimItem.setItemId(item);
						claimItem.setItemName("Obat - Obatan");
						claimItem.setClaimItemAmount(1.0);
						claimItem.setClaimItemValue(totalDrugs);
						
						CaseStatus status = new CaseStatus();
						status.setCaseStatusId(1);
						claimItem.setClaimItemStatus(status);
						claimItem.setDeletedStatus(0);
						
						claimItemService.create(claimItem, user);
					}
				}
				
			}
		}
		catch (Exception e){
			e.printStackTrace();
		}
		return result;
	}




