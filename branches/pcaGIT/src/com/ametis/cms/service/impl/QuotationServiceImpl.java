	public Quotation calculate(Quotation object, ActionUser user)
			throws Exception {
	
		Quotation result = object;
		
		
		try {
			if (object != null){
				String[] eqParam = {"quotationId.quotationId","deletedStatus"};
				Object[] eqValue = {object.getQuotationId(),Integer.valueOf(0)};
				int total = quotationProductService.getTotal(null,null,eqParam,eqValue);
				Collection<QuotationProduct> productList = quotationProductService.search(null,null,eqParam,eqValue,0,total);
				
				double totalPremium = 0.0;
				int totalMember = 0;
				
				for (Iterator iterator = productList.iterator(); iterator.hasNext();) {
					QuotationProduct quotationProduct = (QuotationProduct) iterator
							.next();
					
					if (quotationProduct != null){
						if (quotationProduct.getTotalMaleMember() != null && quotationProduct.getProductId().getMalePremiumRate() != null){
							totalPremium += quotationProduct.getTotalMaleMember() * quotationProduct.getProductId().getMalePremiumRate();
							double totalMaleMember = quotationProduct.getTotalMaleMember() * quotationProduct.getProductId().getMalePremiumRate();
							
							quotationProduct.setQuotationMaleTotal(totalMaleMember);
						}
						if (quotationProduct.getTotalFemaleMember() != null && quotationProduct.getProductId().getFemalePremiumRate() != null){
							totalPremium += quotationProduct.getTotalFemaleMember() * quotationProduct.getProductId().getFemalePremiumRate();
							
							double totalFemaleMember = quotationProduct.getTotalFemaleMember() * quotationProduct.getProductId().getFemalePremiumRate();
							
							quotationProduct.setQuotationFemaleTotal(totalFemaleMember);
						}
						if (quotationProduct.getTotalChildMember() != null && quotationProduct.getProductId().getChildrenPremiumRate() != null){
							totalPremium += quotationProduct.getTotalChildMember() * quotationProduct.getProductId().getChildrenPremiumRate();
							
							double totalChildMember = quotationProduct.getTotalChildMember() * quotationProduct.getProductId().getChildrenPremiumRate();
							
							quotationProduct.setQuotationChildTotal(totalChildMember);
						}
						
						quotationProductService.update(quotationProduct, user);
					}				
				}
				object.setUangPremi(totalPremium);
				result = update(object,user);
			}
		}
		catch (Exception e){
			e.printStackTrace();
		}
		return result;
	} 
